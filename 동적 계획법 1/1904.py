## 문제 풀이 방식
# -------------------------내가 생각한 풀이------------------------
# 00과 1만 가능
# n값은 table[n-2]+'1'+'1', table[n-2]+'00', table[n-1]+'1'의 합
    # table[n-2]+'1'+'1' => table[n-2]+n-1C2
    # table[n-2]+'00' => table[n-2]+n-1C1
    # table[n-1]+'1' => table[n-1]+n-1C1
# [1], [11, 00], [001, 100, 111], [0000, 0011, 1001, 1100, 1111]
# 중복 다 일일이 확인해야 할 것 같음
