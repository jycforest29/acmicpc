## 배운점
# 어떻게 f(n) = f(n-1)+f(n-2)라는 걸 추론할 수 있지?????
# 경계값 주의
# %15746를 나누는 이유

## 걸린 시간

import sys

n = int(sys.stdin.readline())
table = [1, 2]
for i in range(2, n):
    table.append((table[i-1]+table[i-2])%15746)

if n == 1:
    print(table[0])
else:
    print(table[-1])